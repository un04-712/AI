# coding=utf-8
print(10&-5)
"""
10 原码：0000 1010 = 补码
-5 原码：1000 0101
   反码：1111 1010
   补码：1111 1011
10补码&-5补码=0000 1010 & 1111 1011 = 00001010
00001010 = 10
"""

print(~(-5))
"""
-5 补码：1111 1011
~(-5)补码：00000100
00000100 = 4
"""
print(10^(-5))
"""
10补码^-5补码 =0000 1010 ^ 1111 1011 =11110001 
补码->反码：11110001
反码->原码：1111 0000
则十进制为-15
"""
print(~(10^-5))
"""
补码：1111 0001
~(-15)补码：00001110 = 原码
则十进制为14
"""
